    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter("Image",
            "jpg", "jpeg", "png"));
    if (fileChooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
        if (validateFileType(fileChooser.getSelectedFile())) {
            patientImage = fileChooser.getSelectedFile();
            lblFileName.setText(patientImage.getName());
            lblImage.setIcon(new ImageIcon(new ImageIcon(patientImage.getAbsolutePath())
                .getImage().getScaledInstance(100, 100,
                    Image.SCALE_SMOOTH)));
        } else {
            showDialog(null, "Please select Image only!");
        }
        }
    }//GEN-LAST:event_btnBrowseActionPerformed

