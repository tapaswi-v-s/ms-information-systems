    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        Patient patient = new Patient(
            txtFname.getText().trim(),
            txtLname.getText().trim(),
            txtDob.getDate() == null ? null : formatter.format(txtDob.getDate()),
            txtEmail.getText().trim(),
            txtMessage.getText().trim(),
            cmbPatientType.getSelectedItem().toString(),
            radioMale.isSelected() ? "MALE" : "FEMALE",
            patientImage);
        String error = validate(patient);
        if(error.isBlank()){
            
            javax.swing.JLabel titleLabel = (javax.swing.JLabel)splitPane.getTopComponent();
            DisplayPanel display = new DisplayPanel(patient, bottomPanel, titleLabel);
            bottomPanel.add(display);
            CardLayout cl = (CardLayout) bottomPanel.getLayout();
            titleLabel.setText("Patient Details");
            cl.next(bottomPanel);
        }else{
            showDialog(null, error);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

                                             

    String validate(Patient patient) {
        String emailIDRegex = "[^@ \\t\\r\\n]+@[^@ \\t\\r\\n]+\\.[^@ \\t\\r\\n]+";

        Pattern emailPattern = Pattern.compile(emailIDRegex);

        String errorMessage = "";

        /**
         ******* NOT Using If-Else ladder because I want to check for every
         * variable to be empty or not and frame the error message accordingly
         * *******
         */
        if (patient.getfName() == null || patient.getfName().isBlank()) {
            errorMessage += "Please enter your First Name!";
        }
        if (patient.getlName() == null || patient.getlName().isBlank()) {
            errorMessage += (!errorMessage.isBlank() ? "\n" : "")
                    + "Please enter your Last Name!";

        }
        if (patient.getDob() == null || patient.getDob().isBlank()) {
            errorMessage += (!errorMessage.isBlank() ? "\n" : "")
                    + "Please enter your Date of Birth!";

        }else {
            int age = LocalDate.now().getYear() - Integer.parseInt(patient.getDob().split("-")[2]);
            if ((age < 5 || age > 100)) {
                errorMessage += (!errorMessage.isBlank() ? "\n" : "")
                        + "Age should be between 5 to 100 Years!";

            } 
        }
        
        if (patient.getEmail()== null || patient.getEmail().isBlank()) {
            errorMessage += (!errorMessage.isBlank() ? "\n" : "")
                    + "Please enter your Email ID!";

        }else if (!emailPattern.matcher(patient.getEmail()).matches()) {
                errorMessage += (!errorMessage.isBlank() ? "\n" : "")
                        + "Please enter correct Email ID!";
        }
        
        if (patient.getPatientType()== null || patient.getPatientType().toUpperCase().equals("SELECT")) {
            errorMessage += (!errorMessage.isBlank() ? "\n" : "")
                    + "Please select Patient Type!";

        }
        if (patient.getImage() == null) {
            errorMessage += (!errorMessage.isBlank() ? "\n" : "")
                    + "Please select your Profile Picture!";

        } 
        return errorMessage;
    }
    boolean validateFileType(File f) {
        String ext = f.getName().split("\\.")[1].toLowerCase();
        return ("png".equals(ext)
                || "jpg".equals(ext)
                || "jpeg".equals(ext));
    }

    void showDialog(Patient patient, String errorMessage) {
        if (patient == null) {
            JOptionPane.showMessageDialog(this, errorMessage,
                    "Error...!",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            Image image = new ImageIcon(patient.getImage().getAbsolutePath())
                    .getImage().getScaledInstance(100, 100,
                            Image.SCALE_SMOOTH);
            JOptionPane.showMessageDialog(this,
                    patient.toString(),
                    "Details Saved successfully...!",
                    JOptionPane.OK_OPTION,
                    new ImageIcon(image));
        }
    }

